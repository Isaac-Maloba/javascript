// I JS, we need to ahve an initializer before declaring a variable. In ES6, we have two main types of initializers i.e :

// Let : It declares values which can be changed later on
let weight = 13.7
console.log(weight)

// Const: It is used to declare variables that contain values that are not intended to be changed in the entire program
const pi = 3.142
console.log("The Value of Pi is: ",pi)